public class Notas {
    private double notaUm = 4;
    private double notaDois = 7.5;
    private double notaTres = 5;
    private double notaQuatro = 9.3;
    private double totalNotas = 4;
    private double media = (this.notaUm+this.notaDois+this.notaTres+this.notaQuatro)/totalNotas;


    public double getMedia() {
        return media;
    }
}



